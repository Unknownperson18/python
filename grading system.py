#exam grading system
n=int(input())#marks
if(n>=90):
	print("the grade for the student is:A")
elif(n>=80 and n<=89):
	print("the grade for the student is:B")
elif(n>=70 and n<=79):
	print("the grade for the student is:C")
elif(n>=60 and n<=69):
	print("the grade for the student is:D")
else:
	print("failed the exam the grade is:F")	